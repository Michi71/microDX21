// CRS (Coarse Ratio, 0-63) → YM2151 MUL (0-15) lookup table
// Verified: CRS=0→×0.5(MUL=0), CRS=4→×1.0(MUL=1), CRS=22→×7.0(MUL=7)
// Pattern: 4 steps for MUL=0, 3 steps for MUL=1-15, remaining=15
static const uint8_t CRS_TO_MUL[64] = {
     0,  0,  0,  0,  1,  1,  1,  2,  2,  2,  3,  3,  3,  4,  4,  4,
     5,  5,  5,  6,  6,  6,  7,  7,  7,  8,  8,  8,  9,  9,  9, 10,
    10, 10, 11, 11, 11, 12, 12, 12, 13, 13, 13, 14, 14, 14, 15, 15,
    15, 15, 15, 15, 15, 15, 15, 15, 15, 15, 15, 15, 15, 15, 15, 15
};

// DX21 DET (0-6: 0=-3,1=-2,2=-1,3=0,4=+1,5=+2,6=+3) → YM2151 DT1 (0-7)
static const uint8_t DET_TO_DT1[7] = {7, 6, 5, 0, 1, 2, 3};

// DX21 VCED operator parameters (raw, not yet converted to YM2151 registers)
typedef struct {
    uint8_t ar;      // Attack Rate (0-31)
    uint8_t d1r;     // Decay 1 Rate (0-31)
    uint8_t d2r;     // Decay 2 Rate (0-31)
    uint8_t rr;      // Release Rate (0-15)
    uint8_t d1l;     // Decay 1 Level (0-15, DX21: higher = more sustain)
    uint8_t ls;      // Level Scaling sensitivity (0-99)
    uint8_t rs;      // Rate Scaling (0-3, same as YM2151 KS)
    uint8_t ebs;     // EG Bias Sensitivity (0-7)
    uint8_t ame;     // AM Enable (0 or 1)
    uint8_t kvs;     // Key Velocity Sensitivity (0-7)
    uint8_t out;     // Output Level (0-99)
    uint8_t crs;     // Coarse Frequency Ratio (0-63)
    uint8_t det;     // Detune (0-6, DX21 encoding: 0=-3,1=-2,2=-1,3=0,4=+1,5=+2,6=+3)
} DX21_Operator;

// DX21 VCED voice parameters
typedef struct {
    uint8_t alg;       // Algorithm (0-7)
    uint8_t fb;        // Feedback Level (0-7)
    uint8_t lfo_speed;// LFO Speed (0-255)
    uint8_t lfo_delay;// LFO Delay (0-127)
    uint8_t pmd;      // Pitch Modulation Depth (0-127)
    uint8_t amd;      // Amplitude Modulation Depth (0-127)
    uint8_t lfo_sync; // LFO Key Sync (0 or 1)
    uint8_t lfo_wave; // LFO Waveform (0-3: tri, saw, square, s/h)
    uint8_t pms;       // Pitch Modulation Sensitivity (0-7)
    uint8_t ams;       // Amplitude Modulation Sensitivity (0-3)
    int8_t  key_offset;// Transpose in semitones
    DX21_Operator op[4];
    char    name[32];
} DX21_Patch;

